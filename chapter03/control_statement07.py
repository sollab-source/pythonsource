# range(stop): 0부터 stop-1까지의 숫자 생성
for i in range(5):
    print(i)  # 0, 1, 2, 3, 4 출력
# range(start, stop): start부터 stop-1까지의 숫자 생성
for i in range(2, 6):
    print(i)  # 2, 3, 4, 5 출력
# range(start, stop, step): start부터 stop-1까지 step 간격으로 숫자 생성
for i in range(1, 10, 2):
    print(i)  # 1, 3, 5, 7, 9 출력