# 연산자 우선순위 예시
result = 2 + 3 * 4  # 곱셈이 먼저: 3*4=12, 그 다음 덧셈: 2+12=14
print(result)  # 14
result = (2 + 3) * 4  # 괄호 안이 먼저: 2+3=5, 그 다음 곱셈: 5*4=20
print(result)  # 20

# 복합 예시
x = 5
y = 10
z = 2
result = x + y * z / (x - 3)
# 계산 순서:
# 1. (x - 3) = 2
# 2. y * z = 20
# 3. 20 / 2 = 10.0
# 4. x + 10.0 = 15.0
print(result)  # 15.0

'''
같은 우선순위를 가진 연산자들은 일반적으로 왼쪽에서 오른쪽으로 계산된다. 
하지만 지수와 할당 연산자는 오른쪽에서 왼쪽으로 계산된다.
'''
# 왼쪽에서 오른쪽 계산 예시
print(10 - 5 - 2)  # (10 - 5) - 2 = 3

# 오른쪽에서 왼쪽 계산 예시 (지수)
print(2 ** 3 ** 2)  # 2 ** (3 ** 2) = 2 ** 9 = 512

'''
명확성을 위해 복잡한 표현식에서는 괄호를 사용하여 연산 순서를 명시하는 것이 좋다. 
이는 코드를 더 읽기 쉽게 만들고, 혼동을 방지할 수 있다.
'''

# 괄호를 사용한 명확한 표현
result = x + ((y * z) / (x - 3))