fruits = ["apple", "banana", "orange"]

print("apple" in fruits)     # True (fruits에 "apple"이 있으면 True)
print("grape" not in fruits) # True (fruits에 "grape"가 없으면 True)

text = "Hello, world!"
print("world" in text)      # True (text에 "world"가 있으면 True)
print("python" not in text) # True (text에 "python"이 없으면 True)